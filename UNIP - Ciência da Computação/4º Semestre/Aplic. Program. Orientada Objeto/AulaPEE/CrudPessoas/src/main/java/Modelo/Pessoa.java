package Modelo;

public class Pessoa
{
    private int id;
    private String Nome;
    private String RG;
    private String CPF;

    public int getId()
        {
        return id;
        }

    public void setId(int id)
        {
        this.id = id;
        }

    public String getNome()
        {
        return Nome;
        }

    public void setNome(String Nome)
        {
        this.Nome = Nome;
        }

    public String getRG()
        {
        return RG;
        }

    public void setRG(String RG)
        {
        this.RG = RG;
        }

    public String getCPF()
        {
        return CPF;
        }

    public void setCPF(String CPF)
        {
        this.CPF = CPF;
        }
    
    
    
    
    
}
