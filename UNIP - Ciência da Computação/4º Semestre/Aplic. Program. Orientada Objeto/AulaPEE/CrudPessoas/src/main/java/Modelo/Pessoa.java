package Modelo;

public class Pessoa
{
    public int id;
    public String Nome;
    public String RG;
    public String CPF;


    public int getId()
        {
        return id;
        }

    public void setId(int id)
        {
        this.id = id;
        }

    public String getNome()
        {
        return Nome;
        }

    public void setNome(String Nome)
        {
        this.Nome = Nome;
        }

    public String getRG()
        {
        return RG;
        }

    public void setRG(String RG)
        {
        this.RG = RG;
        }

    public String getCPF()
        {
        return CPF;
        }

    public void setCPF(String CPF)
        {
        this.CPF = CPF;
        }
    
}
